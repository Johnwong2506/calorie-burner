// Get current language
export function getCurrentLanguage() {
  return localStorage.getItem('language') || 'en';
}

// Set language
export function setLanguage(lang) {
  localStorage.setItem('language', lang);
}

// Translate text
export function translate(text, lang = null) {
  const currentLang = lang || getCurrentLanguage();
  return translations[currentLang]?.[text] || text;
}

// Get translated text in either direction
function normalizeText(text) {
  if (!text) return '';
  return String(text).replace(/\s+/g, ' ').trim();
}

function getTranslatedText(text, targetLang) {
  const normalized = normalizeText(text);

  // Direct translation available
  if (translations[targetLang]?.[normalized]) {
    return translations[targetLang][normalized];
  }

  // Reverse lookup for consistent toggle (vi -> en)
  const sourceLang = targetLang === 'en' ? 'vi' : 'en';
  const sourceMap = translations[sourceLang] || {};

  for (const key in sourceMap) {
    if (normalizeText(sourceMap[key]) === normalized && translations[targetLang]?.[key]) {
      return translations[targetLang][key];
    }
  }

  return null;
}

// Update all text content on the page
export function updatePageLanguage(lang) {
  // Update page title
  const pageTitle = document.title;
  const translatedTitle = getTranslatedText(pageTitle, lang);
  if (translatedTitle) {
    document.title = translatedTitle;
  }

  // Helper function to translate node text
  function translateNode(node) {
    if (node.nodeType === 3) { // Text node
      const rawText = node.textContent;
      const normalized = normalizeText(rawText);
      const translatedText = getTranslatedText(normalized, lang);
      if (translatedText) {
        const prefix = rawText.match(/^\s*/)[0];
        const suffix = rawText.match(/\s*$/)[0];
        node.textContent = prefix + translatedText + suffix;
      }
    } else if (node.nodeType === 1) { // Element node
      // Skip script tags and style tags (preserve code and AI data)
      if (node.tagName === 'SCRIPT' || node.tagName === 'STYLE') {
        return;
      }
      // Skip chat window elements (preserve AI responses)
      if (node.id === 'chat-window' || node.classList.contains('bot-msg') || node.classList.contains('user-msg')) {
        return;
      }
      // Translate text content if element has only text nodes
      const hasOnlyTextChildren = Array.from(node.childNodes).every(child => 
        child.nodeType === 3 || (child.nodeType === 1 && child.children.length === 0)
      );
      
      if (hasOnlyTextChildren) {
        const rawText = node.textContent;
        const normalized = normalizeText(rawText);
        const translated = getTranslatedText(normalized, lang);
        if (translated) {
          const prefix = rawText.match(/^\s*/)[0];
          const suffix = rawText.match(/\s*$/)[0];
          node.textContent = prefix + translated + suffix;
          return; // Don't traverse children again
        }
      }
      
      // Recursively translate child nodes
      Array.from(node.childNodes).forEach(child => translateNode(child));
    }
  }

  // Start translation from body
  translateNode(document.body);

  // Update placeholders
  document.querySelectorAll('[placeholder]').forEach(element => {
    const key = element.getAttribute('placeholder');
    const translated = getTranslatedText(key, lang);
    if (translated) {
      element.setAttribute('placeholder', translated);
    }
  });

  // Update titles
  document.querySelectorAll('[title]').forEach(element => {
    const key = element.getAttribute('title');
    const translated = getTranslatedText(key, lang);
    if (translated) {
      element.setAttribute('title', translated);
    }
  });

  // Update aria-labels
  document.querySelectorAll('[aria-label]').forEach(element => {
    const key = element.getAttribute('aria-label');
    const translated = getTranslatedText(key, lang);
    if (translated) {
      element.setAttribute('aria-label', translated);
    }
  });

  // Update page language attribute
  document.documentElement.lang = lang;
  if (lang === 'vi') {
    document.body.classList.add('vi-mode');
  } else {
    document.body.classList.remove('vi-mode');
  }

  // Store preference
  setLanguage(lang);
}
